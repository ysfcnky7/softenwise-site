LOCALES = {
    "tr": TR,
    "en": EN,
    "de": DE,
    "fr": FR,
    "ar": AR,
    "ru": RU,
    "es": ES,
    "it": IT,
    "nl": NL,
    "pt": PT,
    "az": AZ,
}


def main() -> None:
    # Validate key parity
    keys = set(TR)
    for code, pack_data in LOCALES.items():
        missing = keys - set(pack_data)
        extra = set(pack_data) - keys
        if missing or extra:
            raise SystemExit(f"{code}: missing={missing} extra={extra}")

    payload = json.dumps(LOCALES, ensure_ascii=False, indent=2)
    OUT.write_text(
        f"/* SoftenWise locales — generated by scripts/build-locales.py */\n"
        f"window.SW_LOCALES = {payload};\n",
        encoding="utf-8",
        newline="\n",
    )
    print(f"Wrote {OUT} ({len(LOCALES)} langs, {len(keys)} keys)")


if __name__ == "__main__":
    main()
